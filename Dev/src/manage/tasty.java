package manage;

/**
 *
 * @author PRANTO
 */
public class tasty {
    
    private String col1;
    private String col2;

    public tasty(String col1, String col2) {
        this.col1 = col1;
        this.col2 = col2;
    }

    /**
     * @return the col1
     */
    public String getCol1() {
        return col1;
    }

    /**
     * @param col1 the col1 to set
     */
    public void setCol1(String col1) {
        this.col1 = col1;
    }

    /**
     * @return the col2
     */
    public String getCol2() {
        return col2;
    }

    /**
     * @param col2 the col2 to set
     */
    public void setCol2(String col2) {
        this.col2 = col2;
    }
    
    
    
}
